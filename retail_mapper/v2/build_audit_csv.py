def title_blocked(target_path: str, title: str) -> bool:
    """Return True if FNDDS override should be vetoed for this title.
    Veto fires when path requires a title signal and none is present.
    """
    required = PATH_TITLE_REQUIRED.get(target_path)
    if required is None:
        return False
    tlow = title.lower()
    return not any(kw in tlow for kw in required)


