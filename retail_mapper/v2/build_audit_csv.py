    print(f"    SR28:   {len(sr_desc):,}")
    print(f"    ESHA:   {len(esha_desc):,}")

    # Index codes from enriched CSV by fdc_id (for fast lookup while streaming cleaned)
    print(f"  indexing {ENRICHED.name}...")
    enriched_by_fdc: dict[str, dict] = {}
    with ENRICHED.open(encoding="utf-8") as fh:
        for r in csv.DictReader(fh):
            fdc = (r.get("fdc_id") or "").strip()
            if fdc:
                enriched_by_fdc[fdc] = {
                    "modifier":         r.get("modifier", ""),
                    "retail_leaf_path": r.get("retail_leaf_path", ""),
                    "fndds_code":       r.get("fndds_code", ""),
                    "sr28_code":        r.get("sr28_code", ""),
                    "esha_code":        r.get("esha_code", ""),
                    "match_source":     r.get("match_source", ""),
                    "match_score":      r.get("match_score", ""),
                    "matched_key":      r.get("matched_key", ""),
                    "portions_json":    r.get("portions_json", ""),
                }
    print(f"    indexed {len(enriched_by_fdc):,} fdc_ids")

    # Stream cleaned CSV, augment, write audit CSV
    print(f"  reading {CLEANED.name} → writing {OUT.name}")
    n = 0
    with CLEANED.open(encoding="utf-8") as src, \
         OUT.open("w", encoding="utf-8", newline="") as dst:
        rdr = csv.DictReader(src)
        # Output columns: cleaned columns + enriched codes + descriptions
        cleaned_cols = list(rdr.fieldnames or [])
        extra_cols = [
            "modifier", "retail_leaf_path",
            "fndds_code", "fndds_desc",
            "sr28_code", "sr28_desc",
            "esha_code", "esha_desc",
            "match_source", "match_score", "matched_key", "portions_json",
        ]
        out_cols = cleaned_cols + [c for c in extra_cols if c not in cleaned_cols]
        wtr = csv.DictWriter(dst, fieldnames=out_cols)
        wtr.writeheader()
        n_fndds_path_override = 0
        n_fndds_id_override = 0
        n_fndds_vetoed = 0
        n_cheese_split = 0
        n_path_rewrite = 0
        n_normalized = 0
        n_fndds_corrected = 0
        n_path_describe = 0
        for row in rdr:
            fdc = (row.get("fdc_id") or "").strip()
            source_category_path = row.get("category_path_fixed", "")
            source_identity = row.get("product_identity_fixed", "")
            source_canonical_path = row.get("canonical_path", "")
            extra = enriched_by_fdc.get(fdc, {})
            f_code = extra.get("fndds_code", "")
            s_code = extra.get("sr28_code", "")
            e_code = extra.get("esha_code", "")
            # Apply DeepSeek FNDDS correction (if any) before any downstream
            # use of f_code. The corrected code feeds the canonical-path map,
            # title-veto, normalizer, and path-rewrite exactly like a fresh
            # matcher result.
            corr = FNDDS_CORRECTIONS.get(fdc)
            if corr:
                f_code = corr[0]
                n_fndds_corrected += 1
            row["modifier"]         = extra.get("modifier", "")
            row["retail_leaf_path"] = extra.get("retail_leaf_path", "")
            row["fndds_code"]       = f_code
            # Prefer corrected desc if provided, else look up from main lookup.
            row["fndds_desc"]       = (corr[1] if corr and corr[1]
                                       else (fndds_desc.get(f_code, "") if f_code else ""))
            row["sr28_code"]        = s_code
            row["sr28_desc"]        = sr_desc.get(s_code, "") if s_code else ""
            row["esha_code"]        = e_code
            row["esha_desc"]        = esha_desc.get(e_code, "") if e_code else ""
            row["match_source"]     = extra.get("match_source", "")
            row["match_score"]      = extra.get("match_score", "")
            row["matched_key"]      = extra.get("matched_key", "")
            row["portions_json"]    = extra.get("portions_json", "")
            # FNDDS-driven canonical override — keyed by fndds_code, ground
            # truth from the cluster audit. Forces SKUs that share an FNDDS
            # code to land at the same canonical_path/identity.
            if f_code:
                # Hand-override map wins over auto-generated FNDDS map.
                canon_path = FNDDS_PATH_OVERRIDES.get(f_code) or FNDDS_CANONICAL_PATH_MAP.get(f_code)
                title_now = row.get("title", "") or ""
                vetoed = canon_path and title_blocked(canon_path, title_now)
                if canon_path and not vetoed and row.get("canonical_path") != canon_path:
                    row["canonical_path"] = canon_path
                    n_fndds_path_override += 1
                if vetoed:
                    n_fndds_vetoed += 1
                canon_id = FNDDS_IDENTITY_OVERRIDES.get(f_code) or FNDDS_CANONICAL_IDENTITY_MAP.get(f_code)
                if canon_id and not vetoed and row.get("product_identity_fixed") != canon_id:
                    row["product_identity_fixed"] = canon_id
                    n_fndds_id_override += 1
            # Cheese type/age sub-router — legacy single-modifier router
            # (now superseded by path_normalizer for any family with a config,
            # but kept as fallback for paths that don't match a config).
            cur = row.get("canonical_path", "")
            new_cheese = reroute_cheese(cur, row.get("title", "") or "")
            if new_cheese != cur:
                row["canonical_path"] = new_cheese
                n_cheese_split += 1
            # Path-quality rewrite — applied BEFORE the normalizer so the
            # rewrite map handles word-order swaps and comma-leaves first.
            new_path = PATH_REWRITE.get(row.get("canonical_path", ""))
            if new_path:
                row["canonical_path"] = new_path
                n_path_rewrite += 1
            # Top-down structural normalizer — chains all applicable tier
            # modifiers (e.g., Mozzarella > Low Moisture > Part Skim) and
            # appends form + alphabetized claims from facet columns.
            if _NORMALIZER_LOADED:
                cur = row.get("canonical_path", "")
                normalized = normalize_path(
                    canonical_path=cur,
                    title=row.get("title", "") or "",
                    form_facet=row.get("form_texture_cut", "") or "",
                    claims_facet=row.get("claims", "") or "",
                )
                if normalized != cur:
                    row["canonical_path"] = normalized
                    n_normalized += 1
            # DeepSeek path-describe correction — final overlay. When the
            # full evidence-based two-pass LLM picked a specific canonical
            # path for this SKU, adopt it. Trumps the canonical-map +
            # normalizer chain.
            pd = PATH_DESCRIBE_CORRECTIONS.get(fdc)
            if pd:
                row["canonical_path"] = pd
                n_path_describe += 1
            # The audit file is a synchronized view, not an independent path
            # mutation target. Legacy FNDDS/path/LLM overlays above are retained
            # for counters while we stabilize the pipeline, but taxonomy columns
            # are finalized from the cleaned category+identity contract.
            row["category_path_fixed"] = source_category_path
            row["product_identity_fixed"] = source_identity
            row["canonical_path"] = source_canonical_path
            apply_finalized_taxonomy(row)
            wtr.writerow(row)
            n += 1
            if n % 100000 == 0:
                print(f"    {n:,} rows", flush=True)
        print(f"    FNDDS path overrides:     {n_fndds_path_override:,}")
        print(f"    FNDDS identity overrides: {n_fndds_id_override:,}")
        print(f"    FNDDS vetoed by title:    {n_fndds_vetoed:,}")
        print(f"    Cheese type/age split:    {n_cheese_split:,}")
        print(f"    Path-rewrite applied:     {n_path_rewrite:,}")
        print(f"    Path normalizer applied:  {n_normalized:,}")
        print(f"    DeepSeek FNDDS corrected: {n_fndds_corrected:,}")
        print(f"    DeepSeek path applied:    {n_path_describe:,}")

    print(f"  wrote {n:,} rows -> {OUT.name} ({OUT.stat().st_size / 1024 / 1024:.1f} MB)")
    # Quick coverage stats
    with OUT.open(encoding="utf-8") as fh:
        rdr = csv.DictReader(fh)
        n_with_fd = n_with_sr = n_with_es = 0
        for r in rdr:
            if r["fndds_desc"]: n_with_fd += 1
            if r["sr28_desc"]:  n_with_sr += 1
            if r["esha_desc"]:  n_with_es += 1
    print(f"    rows with FNDDS desc: {n_with_fd:,} ({100*n_with_fd/max(n,1):.0f}%)")
    print(f"    rows with SR28 desc:  {n_with_sr:,} ({100*n_with_sr/max(n,1):.0f}%)")
    print(f"    rows with ESHA desc:  {n_with_es:,} ({100*n_with_es/max(n,1):.0f}%)")

    # Seed PLU produce stubs from the IFPS list — appended after build so
    # every pipeline run preserves them. Idempotent: skips PLUs already
    # present in the audit (re-runs are safe).
    try:
        import seed_plu_into_master_db as plu_seed  # type: ignore
        print()
        print("  seeding IFPS PLU produce stubs...")
        plu_seed.main()
    except Exception as e:
        print(f"  WARN: PLU seed step failed: {e}")


