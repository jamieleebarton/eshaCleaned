def cheese_subpath(title: str) -> tuple[str, str | None] | None:
    """If title implies a cheese type, return (type_name, optional_modifier).
    Returns None if no cheese type matched."""
    tlow = title.lower()
    cheese_type = None
    for kw, type_name in CHEESE_TYPES:
        if kw in tlow:
            cheese_type = type_name
            break
    if not cheese_type:
        return None
    modifier = None
    for kw, mod_name in CHEESE_MODIFIERS:
        if kw in tlow:
            modifier = mod_name
            break
    return cheese_type, modifier


def reroute_cheese(canonical_path: str, title: str) -> str:
    """When a row sits at the flat 'Dairy > Cheese' bucket and the title
    implies a specific type, push it down into a type subcategory (and
    optional age/style child).
    """
    if canonical_path != "Dairy > Cheese":
        return canonical_path
    sub = cheese_subpath(title)
    if not sub:
        return canonical_path
    cheese_type, modifier = sub
    if modifier:
        return f"Dairy > Cheese > {cheese_type} > {modifier}"
    return f"Dairy > Cheese > {cheese_type}"


def load_fndds_desc() -> dict[str, str]:
    """Hestia's MainFoodDesc.csv first (short codes the canonical tree emits),
    then MainFoodDesc16 (FNDDS 2015-16 long codes) for anything missing."""
    out: dict[str, str] = {}
    if FNDDS_FILE_HESTIA.exists():
        with FNDDS_FILE_HESTIA.open(encoding="utf-8", errors="replace") as fh:
            for r in csv.DictReader(fh):
                code = (r.get("Food code") or "").strip()
                desc = (r.get("Main food description") or "").strip()
                if code and desc and code not in out:
                    out[code] = desc
    if FNDDS_FILE_2016.exists():
        with FNDDS_FILE_2016.open(encoding="utf-8", errors="replace") as fh:
            for r in csv.DictReader(fh):
                code = (r.get("Food code") or "").strip()
                desc = (r.get("Main food description") or "").strip()
                if code and desc and code not in out:
                    out[code] = desc
    return out


def load_sr28_desc() -> dict[str, str]:
    """Build NDB_number → description from the full SR28 reference (7,793 codes,
    includes 236 baby food entries). Falls back to FNDDSSRLinks for any missing."""
    out: dict[str, str] = {}
    if SR_LEGACY.exists() and SR_FOOD.exists():
        ndb_to_fdc: dict[str, str] = {}
        with SR_LEGACY.open(encoding="utf-8", errors="replace") as fh:
            for r in csv.DictReader(fh):
                ndb = (r.get("NDB_number") or "").strip()
                fdc = (r.get("fdc_id") or "").strip()
                if ndb and fdc:
                    ndb_to_fdc[ndb] = fdc
        fdc_to_desc: dict[str, str] = {}
        with SR_FOOD.open(encoding="utf-8", errors="replace") as fh:
            for r in csv.DictReader(fh):
                fdc = (r.get("fdc_id") or "").strip()
                d = (r.get("description") or "").strip()
                if fdc and d:
                    fdc_to_desc[fdc] = d
        for ndb, fdc in ndb_to_fdc.items():
            d = fdc_to_desc.get(fdc)
            if d:
                out[ndb] = d
    if SR_LINKS.exists():
        with SR_LINKS.open(encoding="utf-8", errors="replace") as fh:
            for r in csv.DictReader(fh):
                code = (r.get("SR code") or "").strip()
                desc = (r.get("SR description") or "").strip()
                if code and desc and code not in out:
                    out[code] = desc
    return out


def load_esha_desc() -> dict[str, str]:
    out: dict[str, str] = {}
    if not ESHA_FILE.exists():
        return out
    with ESHA_FILE.open(encoding="utf-8") as fh:
        for r in csv.DictReader(fh):
            code = (r.get("EshaCode") or "").strip()
            desc = (r.get("Description") or "").strip()
            if code and desc:
                out[code] = desc
    return out


